#!/usr/bin/env node
/**
 * graphify-postprocess.mjs
 * Rewrites GRAPH_REPORT.md from code-review-graph's SQLite database.
 * Replaces old graphify JSON dependency with direct SQL queries.
 *
 * Chained via: npm run graphify:build
 * Output: .toon/graphify/GRAPH_REPORT.md
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import Database from 'better-sqlite3';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const DB_PATH = path.join(ROOT, '.code-review-graph', 'graph.db');
const REPORT_OUT = path.join(ROOT, '.toon', 'graphify', 'GRAPH_REPORT.md');

if (!fs.existsSync(DB_PATH)) {
  console.error('graph.db not found — run code-review-graph build first');
  process.exit(1);
}

const db = new Database(DB_PATH, { readonly: true });

// ── Stats ────────────────────────────────────────────────────────────────
const date = new Date().toISOString().split('T')[0];
const nodeCount = db.prepare("SELECT COUNT(*) as c FROM nodes").get().c;
const edgeCount = db.prepare("SELECT COUNT(*) as c FROM edges").get().c;
const fileCount = db.prepare("SELECT COUNT(*) as c FROM nodes WHERE kind = 'File'").get().c;
const communityCount = db.prepare("SELECT COUNT(*) as c FROM communities").get().c;

// ── Top hub nodes (most referenced via edges) ────────────────────────────
const hubs = db.prepare(`
  SELECT n.name, n.kind, COUNT(e.id) as refs
  FROM nodes n
  JOIN edges e ON e.target_qualified = n.qualified_name
  WHERE e.kind = 'IMPORTS_FROM' AND e.confidence > 0.5
  GROUP BY n.id
  ORDER BY refs DESC
  LIMIT 15
`).all();

// ── Community breakdown ──────────────────────────────────────────────────
const communities = db.prepare(`
  SELECT c.name, c.cohesion, COUNT(n.id) as node_count
  FROM communities c
  LEFT JOIN nodes n ON n.community_id = c.id
  GROUP BY c.id
  ORDER BY node_count DESC
  LIMIT 15
`).all();

// ── Languages ────────────────────────────────────────────────────────────
const fileCounts = db.prepare(`
  SELECT language, COUNT(*) as c
  FROM nodes
  WHERE kind = 'File'
  GROUP BY language
  ORDER BY c DESC
`).all();

db.close();

// ── Helpers ──────────────────────────────────────────────────────────────
function stripRoot(name) {
  return name.replace(ROOT + '/', '');
}
function esc(s) {
  return s || '?';
}

// ── Render ────────────────────────────────────────────────────────────────
const lines = [
  '# Graph Report — YVON (' + date + ')',
  '> **' + nodeCount + ' nodes · ' + edgeCount.toLocaleString() + ' edges · ' + communityCount + ' communities · ' + fileCount + ' files**',
  '> Rebuild: `npm run graphify:build` | Serve: `npm run graphify:serve` | Open UI: `/api/graph-html`',
  '',
  '---',
  '',
  '## Hub Symbols (highest impact — most referenced)',
  '',
  '| Rank | Symbol | Kind | References |',
  '|------|--------|------|------------|',
];

for (let i = 0; i < hubs.length; i++) {
  const h = hubs[i];
  lines.push('| ' + (i + 1) + ' | `' + esc(stripRoot(h.name)) + '` | ' + esc(h.kind) + ' | **' + h.refs + '** |');
}

lines.push(
  '',
  '> Changing a hub symbol affects every file that references it.',
  '',
  '---',
  '',
  '## Communities',
  '',
  '| # | Community | Cohesion | Nodes |',
  '|---|-----------|----------|-------|'
);

for (const c of communities) {
  lines.push('| | **' + esc(c.name) + '** | ' + (c.cohesion ? c.cohesion.toFixed(2) : 'N/A') + ' | ' + c.node_count + ' |');
}

lines.push(
  '',
  '---',
  '',
  '## Language Distribution',
  '',
  '| Language | Files |',
  '|-----------|-------|'
);

for (const f of fileCounts) {
  lines.push('| ' + esc(f.language) + ' | ' + f.c + ' |');
}

lines.push(
  '',
  '---',
  '',
  '## Interactive UI',
  '',
  'Open the interactive D3.js visualization:',
  '- Local: `open .toon/graphs/graph.html`',
  '- Dev server: `npm run graphify:serve` → http://localhost:8765',
  '- API route: `/api/graph-html` (in app)',
  '',
  '---',
  '',
  '_Generated by `scripts/graphify-postprocess.mjs` (code-review-graph v2.3.6) on ' + date + '_'
);

const dir = path.dirname(REPORT_OUT);
if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
fs.writeFileSync(REPORT_OUT, lines.join('\n'));
console.log('✓ GRAPH_REPORT.md written → .toon/graphify/ (' + nodeCount + ' nodes, ' + edgeCount.toLocaleString() + ' edges, ' + communityCount + ' communities)');
